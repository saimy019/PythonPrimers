temperature = float(input("Enter teh temperature "))

if temperature >= 40:
    print("Way too hot - stay inside!")

elif temperature >= 30:
    print("Hot - beach time!")

elif temperature >= 20:
    print("Lovely day - how about a picnic?!")

elif temperature >= 10:
    print ("On thr cold side - better take a jacket")

elif temperature < 10:
    print("Way too cold - stoke up the fire")
